# OBD Scanner (Flutter)

Диагностика автомобиля через ELM327 (BLE / Bluetooth Classic / Wi-Fi). Аналог Torque Pro / Car Scanner.

## Что внутри

| Слой | Файлы | Ответственность |
|------|-------|-----------------|
| UI | `lib/ui/*` | Дашборд со стрелочными приборами, панель DTC |
| State | `lib/state/connection_provider.dart` | Riverpod, конечный автомат соединения |
| Domain | `lib/core/obd_service.dart` | Инициализация адаптера, poll loop, безопасность |
| Core | `lib/core/{obd_connection,request_queue,obd_parsers}.dart` | Сборка ответов, очередь, HEX-парсинг |
| Transport | `lib/transport/*` | BLE / BT Classic / Wi-Fi за единым интерфейсом |

Зависимости однонаправлены: `UI → State → ObdService → Transport`. Парсеры — чистые функции (покрыты тестами в `test/parsers_test.dart`).

## Запуск

См. подробную инструкцию ниже в разделе «Как запустить».

```bash
flutter pub get
flutter run                 # на реальном устройстве (эмулятор не имеет BT)
flutter test                # проверка парсеров
```

> На iOS Bluetooth Classic недоступен — используй BLE или Wi-Fi.
> Для Wi-Fi-адаптеров сначала подключи телефон к сети адаптера, затем выбери вкладку «Wi-Fi».

> ⚠️ Лицензия: `flutter_blue_plus` бесплатен для личных/учебных проектов,
> но требует платную лицензию для коммерческого использования. Если планируешь
> продавать приложение — рассмотри `universal_ble` как замену BLE-транспорта.

## Платформенная настройка

- **Android**: разрешения уже прописаны в `android/app/src/main/AndroidManifest.xml`.
- **iOS**: перенеси ключи из `ios/Runner/Info.plist.snippet.xml` в свой `Info.plist`.

## Как работает обмен

1. Команды — ASCII со завершающим `\r` (`010C\r`).
2. Конец ответа адаптера — символ `>` (0x3E); `ObdConnection` копит буфер до него.
3. Все команды идут через `RequestQueue` — строго по одной (ELM327 полудуплексный).
4. Ответ `41 0C A B` парсится: `(A*256+B)/4` = RPM.

## Безопасность

- Приложение работает в read-only режимах OBD (01/03/09).
- `Mode 04` (сброс DTC) — только при `V=0` и явном подтверждении пользователя (fail-safe).
- Команды записи никогда не попадают в автоматический poll loop.
- Произвольный ввод HEX-команд не реализован намеренно.

## Поддерживаемые PID

Базовые: RPM (0C), скорость (0D), темп. ОЖ (05), нагрузка (04), дроссель (11).
Расширенные: темп. впуска (0F), уровень топлива (2F), MAF (10), наддув/MAP (0B),
давление топлива (0A), опережение зажигания (0E), темп. за бортом (46), лямбда (24),
пробег с момента сброса DTC (31), напряжение (ATRV), мгновенный расход топлива (расчётный из MAF).

Перед опросом приложение запрашивает маски поддержки (0100/0120/0140) и опрашивает
только те расширенные PID, что реально поддержаны ЭБУ конкретного авто — это экономит
шину и не плодит таймауты. Новый PID добавляется одной строкой в каталог `Pid` + парсер.
